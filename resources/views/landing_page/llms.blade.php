# FiscalDock

> Plataforma brasileira de inteligência fiscal para contadores e escritórios contábeis. Importa arquivos SPED (EFD ICMS/IPI e EFD Contribuições), monitora CNPJs de participantes, valida notas fiscais (clearance NF-e) e detecta riscos fiscais antes da auditoria da Receita.

## O que o FiscalDock faz

- **Importação EFD ICMS/IPI**: leitura completa dos blocos C, D, E, H — extração de notas, itens, apuração de ICMS e inventário
- **Importação EFD Contribuições**: blocos A, M, F — apuração de PIS/COFINS, retenções na fonte, serviços tomados
- **Monitoramento de CNPJs**: situação cadastral, regime tributário, Simples Nacional, certidões
- **Clearance de NF-e**: validação de documentos fiscais contra SEFAZ, detecção de notas canceladas e frias
- **Consultas tributárias**: CNPJ completo, score de risco fiscal, certidões CND
- **BI Fiscal**: dashboards de cruzamento (apuração × notas), ranking de participantes, análise por CFOP

## Para quem

Contadores, escritórios contábeis e departamentos fiscais de empresas brasileiras que precisam de previsibilidade fiscal, redução de retrabalho em revisão mensal de EFD e automação de consultas em lote de participantes.

