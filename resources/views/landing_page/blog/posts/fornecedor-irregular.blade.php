<p>Um fornecedor com CNPJ baixado, IE suspensa ou listado no CEIS pode custar caro ao seu cliente. Creditos tributarios tomados de fornecedores irregulares podem ser glosados em auditoria, resultando em multas que chegam a 75% do valor do credito, alem de juros SELIC.</p>

<p>A boa noticia: e possivel identificar esses riscos antes que o fisco encontre. Veja como.</p>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">Por Que Fornecedores Irregulares Sao um Risco</h2>

<p>Quando seu cliente compra de um fornecedor e toma credito de ICMS, PIS ou COFINS, ele esta confiando que aquele fornecedor esta regular perante o fisco. Se nao estiver, o credito pode ser considerado indevido.</p>

<p>Os cenarios mais comuns:</p>

<ul class="list-disc pl-6 space-y-2 my-4">
    <li><strong>CNPJ baixado ou inapto:</strong> A empresa deixou de existir ou foi declarada inapta pela Receita Federal. Notas emitidas apos o encerramento sao inidoneas.</li>
    <li><strong>IE cancelada ou suspensa:</strong> A inscricao estadual e obrigatoria para operacoes com ICMS. Sem IE valida, o credito de ICMS pode ser glosado.</li>
    <li><strong>Empresa no CEIS:</strong> Empresas listadas no Cadastro de Empresas Inidoneas e Suspensas foram punidas por fraudes. Operar com elas gera risco legal e reputacional.</li>
    <li><strong>Optante pelo Simples indevidamente:</strong> Se o fornecedor e Simples Nacional, o regime de credito de PIS/COFINS muda. Tomar credito cheio de fornecedor Simples e uma irregularidade comum.</li>
</ul>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">Como Identificar na Pratica</h2>

<h3 class="text-xl font-bold text-gray-900 mt-8 mb-3">1. Consulta a Receita Federal (CNPJ)</h3>

<p>A consulta de CNPJ na Receita Federal revela a situacao cadastral (Ativa, Baixada, Inapta, Suspensa), o QSA (Quadro de Socios e Administradores), CNAEs e endereco. E a primeira verificacao que deve ser feita.</p>

<h3 class="text-xl font-bold text-gray-900 mt-8 mb-3">2. Consulta ao SINTEGRA</h3>

<p>O SINTEGRA (Sistema Integrado de Informacoes sobre Operacoes Interestaduais com Mercadorias e Servicos) permite verificar a IE do fornecedor por estado. Uma IE cancelada ou nula indica que o fornecedor nao pode emitir notas com ICMS.</p>

<h3 class="text-xl font-bold text-gray-900 mt-8 mb-3">3. Consulta ao CEIS</h3>

<p>O Portal da Transparencia do governo federal disponibiliza o CEIS com todas as empresas punidas. E essencial para due diligence, especialmente se o cliente participa de licitacoes.</p>

<h3 class="text-xl font-bold text-gray-900 mt-8 mb-3">4. Verificacao de Simples Nacional</h3>

<p>A consulta a Receita Federal sobre opcao pelo Simples Nacional confirma se o fornecedor e optante e desde quando. Isso impacta diretamente o calculo de creditos de PIS/COFINS.</p>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">O Problema: Fazer Isso Manualmente</h2>

<p>Para um unico fornecedor, essas consultas levam alguns minutos. Mas um SPED tipico pode ter dezenas ou centenas de participantes. Verificar cada um manualmente, em 3 ou 4 fontes diferentes, e inviavel na pratica.</p>

<p>E por isso que a maioria dos escritorios nao faz essa verificacao — e so descobre o problema quando o fisco bate a porta.</p>

