<p>Para contadores e escritorios contabeis, monitorar riscos fiscais nao e opcional — e o que separa um escritorio reativo de um proativo. Fornecedores irregulares, notas com problemas e creditos tributarios indevidos podem gerar multas pesadas para seus clientes.</p>

<p>Neste artigo, listamos os <strong>5 riscos fiscais mais comuns</strong> que todo contador deveria monitorar continuamente.</p>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">1. Fornecedor com CNPJ Irregular</h2>

<p>Um fornecedor com CNPJ baixado, suspenso ou inapto na Receita Federal pode invalidar creditos tributarios inteiros. Se o seu cliente tomou credito de ICMS ou PIS/COFINS de uma nota emitida por um fornecedor nessa situacao, o fisco pode glosar o credito e aplicar multa.</p>

<p><strong>Como monitorar:</strong> Consultas periodicas a Receita Federal via CNPJ. No FiscalDock, isso e feito automaticamente com consultas em lote e alertas quando o status muda.</p>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">2. Inscricao Estadual (IE) Suspensa ou Cancelada</h2>

<p>A IE e obrigatoria para operacoes com ICMS. Se um fornecedor tem IE cancelada ou suspensa no SINTEGRA, as notas emitidas por ele podem ser consideradas inidôneas, comprometendo creditos de ICMS.</p>

<p><strong>Como monitorar:</strong> Consulta ao SINTEGRA por estado. O FiscalDock integra com SINTEGRA para verificar a situacao da IE de cada participante extraido do SPED.</p>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">3. Empresa Listada no CEIS (Cadastro de Empresas Inidoneas)</h2>

<p>O CEIS e um cadastro federal de empresas punidas por fraude em licitacoes, contratos publicos ou outras irregularidades. Operar com fornecedores listados no CEIS pode gerar risco reputacional e legal, especialmente para empresas que participam de licitacoes.</p>

<p><strong>Como monitorar:</strong> Consulta ao Portal da Transparencia. O FiscalDock verifica automaticamente se participantes do SPED estao listados no CEIS.</p>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">4. Fornecedor Optante pelo Simples Nacional</h2>

<p>Quando um fornecedor e optante pelo Simples Nacional, o regime de apuracao de PIS/COFINS muda. Creditos tomados indevidamente de fornecedores do Simples podem ser glosados em auditoria.</p>

<p><strong>Como monitorar:</strong> Consulta a Receita Federal sobre opcao pelo Simples. O FiscalDock enriquece os dados dos participantes com essa informacao automaticamente.</p>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">5. Divergencias entre SPED e Notas Fiscais</h2>

<p>Divergencias nos valores declarados no SPED (EFD ICMS/IPI ou PIS/COFINS) e os dados reais das notas fiscais sao um dos principais gatilhos para malha fiscal. Valores de VL_DOC inconsistentes, CFOPs incorretos ou notas nao declaradas geram alertas no SEFAZ.</p>

<p><strong>Como monitorar:</strong> Importacao e analise do SPED com cruzamento de dados. O FiscalDock extrai automaticamente notas por bloco (A, C, D) e permite analise por CFOP, participante e periodo nos dashboards.</p>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">Conclusao</h2>

<p>Monitorar esses 5 riscos nao precisa ser um processo manual e demorado. Com uma plataforma como o FiscalDock, voce importa o SPED, extrai participantes automaticamente e recebe alertas quando algo muda — tudo em minutos, nao em dias.</p>

<p>O papel do contador continua sendo fundamental na interpretacao e na tomada de decisao. Mas a coleta de dados e a deteccao de riscos podem (e devem) ser automatizadas.</p>
